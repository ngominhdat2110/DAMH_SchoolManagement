    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(jtfMaCB.getText().equals("") || jtfHoTen.getText().equals("") || jtfSoDT.getText().equals("") || jtfDiaChi.getText().equals("") ||
            jtfNamSinh.getText().equals("") || jComboBox2.getSelectedIndex() == 0 || jtfSoTaiKhoan.getText().equals("") || jComboBox1.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(this, "Hãy Nhập Đầy Đủ Dữ Liệu");
        } else{
            CanBo st = new CanBo() {
            };
            st.setMaCB(Integer.parseInt(jtfMaCB.getText()));
            st.setHoTen(jtfHoTen.getText());
            st.setSoDT(jtfSoDT.getText());
            st.setDiaChi(jtfDiaChi.getText());
            st.setNamSinh(jtfNamSinh.getText());
            st.setChucVu(jComboBox2.getSelectedItem().toString());
            st.setSoTaiKhoan(jtfSoTaiKhoan.getText());
            String str = jComboBox1.getSelectedItem().toString();
            if(str.equals("Bậc 1")) st.setBac(1);
            else if(str.equals("Bậc 2")) st.setBac(2);
            else if(str.equals("Bậc 3")) st.setBac(3);
            else if(str.equals("Bậc 4")) st.setBac(4);
            else if(str.equals("Bậc 5")) st.setBac(5);
            else if(str.equals("Bậc 6")) st.setBac(6);
            else if(str.equals("Bậc 7")) st.setBac(7);
            else if(str.equals("Bậc 8")) st.setBac(8);

            ConnectionSQL.UpdateCanBo(st);
            JOptionPane.showMessageDialog(null, "Cập Nhật Thành Công");
            showData(ConnectionSQL.findAllCanBo());}
    }//GEN-LAST:event_btnUpdateActionPerformed

