public void showData(List<CanBo>canbol){
    if(canbol.size() == 0) System.out.println("Rỗng");
    List<CanBo>listCanBo = new ArrayList<>();
    listCanBo=canbol;
    DefaultTableModel tableModel;
    jTable.getModel();
    tableModel=(DefaultTableModel)jTable.getModel();
    tableModel.setRowCount(0);
    listCanBo.forEach((canbo) -> {
        tableModel.addRow(new Object[]{
            canbo.getMaCB(), canbo.getHoTen(), canbo.getSoDT(), canbo.getDiaChi(), canbo.getNamSinh(),
            canbo.getChucVu(), canbo.getSoTaiKhoan(), canbo.getBac()
    });
    });
            jTable.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(jTable.getSelectedRow() >= 0){
                    jtfMaCB.setText(jTable.getValueAt(jTable.getSelectedRow(), 0) + "");
                    jtfHoTen.setText(jTable.getValueAt(jTable.getSelectedRow(), 1) + "");
                    jtfSoDT.setText(jTable.getValueAt(jTable.getSelectedRow(), 2) + "");
                    jtfDiaChi.setText(jTable.getValueAt(jTable.getSelectedRow(), 3) + "");
                    jtfNamSinh.setText(jTable.getValueAt(jTable.getSelectedRow(), 4) + "");
                    jComboBox2.setSelectedItem(jTable.getValueAt(jTable.getSelectedRow(), 5) + "");
                    jtfSoTaiKhoan.setText(jTable.getValueAt(jTable.getSelectedRow(), 6) + "");
                    jComboBox1.setSelectedItem(jTable.getValueAt(jTable.getSelectedRow(), 7) + "");
                    maCbGlobal = Integer.parseInt(jTable.getValueAt(jTable.getSelectedRow(), 0).toString());
//                  System.out.println("MaCB global co gia tri la: "+maCbGlobal);
                }
            }
        });
}


